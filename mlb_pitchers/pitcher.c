void Pitcher_print(const Pitcher* p)
{
  char name[24];
  sprintf(name, "%s %s", p->lastName, p->firstName);
  printf(
    "%-13s %-3s %2d %2d %5.2f "
    "%2d %2d %2d %3d %5.1f "
    "%2d %2d %2d %2d %2d %2d "
    "%5.3f %4.2f\n",
    name, p->team, p->win, p->lose, p->era,
    p->games, p->gamesStarted, p->saves, p->saveOppertunities, p->inningsPitched,
    p->hits, p->runs, p->earnedRuns, p->homeRuns, p->basesOnBalls, p->strikeOuts,
    p->battingAverage, p->whip
  );
}

void Pitcher_printAll(int count)
{
  if (count == 0) {
    count = pitcher_count;
  }
  printf(
    "%-13s %-3s %2s %2s %5s "
    "%2s %2s %2s %3s %5s "
    "%2s %2s %2s %2s %2s %2s "
    "%5s %4s\n",
    "Name", "Tm", "W", "L", "ERA",
    "G", "GS", "SV", "SVO", "IP ",
    "H", "R", "ER", "HR", "BB", "SO",
    "AVG ", "WHIP");
  // Tm = Team, W = Wins, L = Loses, ERA = Earned Runs Average
  // G = Games, GS = Games Started, SV = Saves, SVO = Save Opportunity
  // IP = Innings Pitched, H = Hits, R = Runs, ER = Earned Runs
  // HR = Homeruns, BB = Base on balls (walk), SO = Strikeouts
  // AVG = Hit Average, WHIP = (Walks+Hits)/IP
  for (int i = 0; i < count; i++) {
    Pitcher_print(&pitchers[i]);
  }
}

